    @Test
    func spoonsAreSortedByName() {
        // Stable diffs when the user enables/disables Spoons in any
        // order. Output ordering must come from name sort, not state
        // iteration order.
        let entryA = entry("Alpha", hasConfigure: true, hasStart: true,
                           hotkeyActions: [])
        let entryZ = entry("Zebra", hasConfigure: true, hasStart: true,
                           hotkeyActions: [])
        var state = AppState()
        state.spoons["Zebra"] = SpoonState(
            sourceID: "catokolas", enabled: true,
            installedRef: .gitCommit("z"))
        state.spoons["Alpha"] = SpoonState(
            sourceID: "catokolas", enabled: true,
            installedRef: .gitCommit("a"))
        let snippet = SnippetGenerator.generate(
            state: state,
            catalog: ["Alpha": entryA, "Zebra": entryZ],
            repos: ["catokolas": catokolasRepo],
            timestamp: fixedDate)
        let alphaIdx = snippet.range(of: ":andUse(\"Alpha\"")!.lowerBound
        let zebraIdx = snippet.range(of: ":andUse(\"Zebra\"")!.lowerBound
        #expect(alphaIdx < zebraIdx)
    }

    @Test
    func multipleSourcesRegisterOnlyTheCustomOnes() {
        // Mix of catokolas Spoons and upstream Spoons — only catokolas
        // gets a `repos[...]` registration line.
        let ffm = entry("FocusFollowsMouse",
                        hasConfigure: true, hasStart: true,
                        hotkeyActions: ["toggle"])
        let caf = entry("Caffeine",
                        hasConfigure: false, hasStart: true,
                        hotkeyActions: [])
        var state = AppState()
        state.spoons["FocusFollowsMouse"] = SpoonState(
            sourceID: "catokolas", enabled: true,
            installedRef: .gitCommit("a"))
        state.spoons["Caffeine"] = SpoonState(
            sourceID: "hammerspoon-official", enabled: true,
            installedRef: .zipETag(value: "etag", fetchedAt: fixedDate))
        let snippet = SnippetGenerator.generate(
            state: state,
            catalog: ["FocusFollowsMouse": ffm, "Caffeine": caf],
            repos: [
                "catokolas":            catokolasRepo,
                "hammerspoon-official": .default,
            ],
            timestamp: fixedDate)
        // Exactly one repo registration line.
        let repoLines = snippet.components(separatedBy: "\n")
            .filter { $0.contains("spoon.SpoonInstall.repos[") }
        #expect(repoLines.count == 1)
        #expect(repoLines[0].contains("\"catokolas\""))
    }

    // MARK: - Helpers

    private let catokolasRepo: RepoRef = .custom(
        id: "catokolas",
        url: "https://github.com/catokolas/HS_SpoonsContrib",
        branch: "main",
        desc: "Cato Kolås's contributed Spoons")

    private func entry(
        _ name: String,
        hasConfigure: Bool = true,
        hasStart: Bool = true,
        hotkeyActions: [String] = []
    ) -> SpoonCatalogEntry {
        return SpoonCatalogEntry(
            id: "test:\(name)",
            name: name,
            sourceID: "catokolas",
            metadata: SpoonMetadata(version: "0.1", description: nil,
                                    author: nil, homepage: nil, license: nil),
            lifecycle: Lifecycle(
                hasStart: hasStart, hasStop: true, hasToggle: false,
                hasConfigure: hasConfigure, eventDriven: false),
            config: [],
            hotkeys: hotkeyActions.map { action in
                HotkeyAction(action: action, label: nil, default: nil)
            },
            provenance: .manifest)
    }

    /// Variant that lets tests give actions an authored `default`.
    /// Used by the manifest-default fallback tests; the no-default
    /// helper above stays minimal for older tests. Different argument
    /// label so neither overload becomes ambiguous when the caller
    /// omits `hotkeyActions`.
    private func entry(
        _ name: String,
        hasConfigure: Bool = true,
        hasStart: Bool = true,
        hotkeyDefaults: [(String, HotkeyBinding?)]
    ) -> SpoonCatalogEntry {
        return SpoonCatalogEntry(
            id: "test:\(name)",
            name: name,
            sourceID: "catokolas",
            metadata: SpoonMetadata(version: "0.1", description: nil,
                                    author: nil, homepage: nil, license: nil),
            lifecycle: Lifecycle(
                hasStart: hasStart, hasStop: true, hasToggle: false,
                hasConfigure: hasConfigure, eventDriven: false),
            config: [],
            hotkeys: hotkeyDefaults.map { (action, def) in
                HotkeyAction(action: action, label: nil, default: def)
            },
            provenance: .manifest)
    }
}
