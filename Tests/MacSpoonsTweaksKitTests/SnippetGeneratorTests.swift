    // Pinned timestamp so golden-string comparisons are stable.
    private let fixedDate = ISO8601DateFormatter().date(
        from: "2026-06-08T12:34:56Z")!

    // MARK: - Headers and scaffolding

    @Test
    func emptyStateProducesHeaderOnly() {
        let snippet = SnippetGenerator.generate(
            state: AppState(),
            catalog: [:], repos: [:],
            timestamp: fixedDate)
        #expect(snippet.contains(SnippetGenerator.managedMarker))
        #expect(snippet.contains("Generated by MacSpoonsTweaks at 2026-06-08T12:34:56Z"))
        #expect(snippet.contains("require(\"mac_spoons_tweaks\")"))
        #expect(snippet.contains("hs.loadSpoon(\"SpoonInstall\")"))
        #expect(snippet.contains("-- No Spoons enabled."))
        #expect(!snippet.contains(":andUse("))
    }

    @Test
    func disabledSpoonsAreSkipped() {
        var state = AppState()
        // Enabled but NOT yet installed: still emit. `andUse` installs
        // on demand from Hammerspoon start, so the user's Apply
        // before-installation is honored.
        state.spoons["AndUseHandlesIt"] = SpoonState(
            sourceID: "catokolas", enabled: true, installedRef: nil)
        // Disabled → skip regardless of install state.
        state.spoons["Disabled"] = SpoonState(
            sourceID: "catokolas", enabled: false,
            installedRef: .gitCommit("abc"))
        let snippet = SnippetGenerator.generate(
            state: state,
            catalog: ["AndUseHandlesIt": entry("AndUseHandlesIt"),
                      "Disabled":        entry("Disabled")],
            repos: ["catokolas": catokolasRepo],
            timestamp: fixedDate)
        #expect(snippet.contains(":andUse(\"AndUseHandlesIt\""),
                "enabled-but-uninstalled Spoons still get an andUse block")
        #expect(!snippet.contains("Disabled"))
    }

    @Test
    func spoonMissingFromCatalogIsSkippedRatherThanCrashing() {
        // State references a Spoon we don't have a catalog entry for —
        // typically a stale state.json after a catalog removal. Snippet
        // generation must not crash or produce broken Lua.
        var state = AppState()
        state.spoons["Ghost"] = SpoonState(
            sourceID: "catokolas", enabled: true,
            installedRef: .gitCommit("abc"))
        let snippet = SnippetGenerator.generate(
            state: state, catalog: [:], repos: [:],
            timestamp: fixedDate)
        #expect(!snippet.contains("Ghost"))
    }

    // MARK: - andUse shape: catokolas (hasConfigure) Spoon

    @Test
    func catokolasSpoonWithConfigureUsesFnDeepMergePattern() {
        let entry = entry(
            "FocusFollowsMouse",
            hasConfigure: true, hasStart: true,
            hotkeyActions: ["toggle"])
        var state = AppState()
        state.spoons["FocusFollowsMouse"] = SpoonState(
            sourceID: "catokolas",
            enabled: true,
            installedRef: .gitCommit("abc"),
            config: [
                "delay": .number(0.05),
                "excludedApps": .stringList(["Notification Center"]),
            ],
            hotkeys: ["toggle": HotkeyBinding(mods: ["ctrl","cmd"], key: "f")])

        let snippet = SnippetGenerator.generate(
            state: state,
            catalog: ["FocusFollowsMouse": entry],
            repos: ["catokolas": catokolasRepo],
            timestamp: fixedDate)

        // The repo registration block appears once, with all fields.
        #expect(snippet.contains("spoon.SpoonInstall.repos[\"catokolas\"]"))
        #expect(snippet.contains("url = \"https://github.com/catokolas/HS_SpoonsContrib\""))
        #expect(snippet.contains("branch = \"main\""))

        // andUse block — fn = function(s) s:configure({...}) end form.
        #expect(snippet.contains(":andUse(\"FocusFollowsMouse\", {"))
        #expect(snippet.contains("repo = \"catokolas\","))
        #expect(snippet.contains(
            "fn = function(s) s:configure({ delay = 0.05, " +
            "excludedApps = { \"Notification Center\" } }) end,"))
        #expect(snippet.contains(
            "hotkeys = { toggle = { { \"ctrl\", \"cmd\" }, \"f\" } },"))
        #expect(snippet.contains("start = true,"))
    }

    // MARK: - andUse shape: upstream (no :configure) Spoon

    @Test
    func upstreamSpoonWithoutConfigureUsesFlatConfigForm() {
        let entry = entry(
            "Caffeine",
            hasConfigure: false, hasStart: true,
            hotkeyActions: ["toggle"])
        var state = AppState()
        state.spoons["Caffeine"] = SpoonState(
            sourceID: "hammerspoon-official",
            enabled: true,
            installedRef: .zipETag(value: "W/\"abc\"", fetchedAt: fixedDate),
            config: ["show_notifications": .bool(true)],
            hotkeys: ["toggle":
                HotkeyBinding(mods: ["ctrl","alt","cmd"], key: "k")])

        let snippet = SnippetGenerator.generate(
            state: state,
            catalog: ["Caffeine": entry],
            // sourceID is "hammerspoon-official", but repos[sourceID] = .default
            // means SpoonInstall's built-in "default" is used and we don't
            // emit a registration.
            repos: ["hammerspoon-official": .default],
            timestamp: fixedDate)

        // No repo registration for default.
        #expect(!snippet.contains("spoon.SpoonInstall.repos["))
        // Flat config form (no fn wrapper). The andUse block emits the
        // SpoonInstall repo NAME ("default"), not the catalog source
        // id ("hammerspoon-official") — that's the only string
        // spoon.SpoonInstall.repos knows about for upstream Spoons.
        #expect(snippet.contains("repo = \"default\","))
        #expect(!snippet.contains("repo = \"hammerspoon-official\","))
        #expect(snippet.contains("config = { show_notifications = true },"))
        #expect(!snippet.contains("fn = function"))
        #expect(snippet.contains("start = true,"))
    }

    // MARK: - Nesting

    @Test
    func deeplyNestedConfigSurvivesEncoding() {
        // Matches the MouseTrackpadTweaks shape — the worst case for
        // the recursive encoder, and the failure mode the deep-merge
        // (`fn` form) was designed to avoid: changing one nested key
        // must leave its siblings untouched on the Spoon side.
        let entry = entry(
            "MouseTrackpadTweaks",
            hasConfigure: true, hasStart: true,
            hotkeyActions: ["toggle"])
        var state = AppState()
        state.spoons["MouseTrackpadTweaks"] = SpoonState(
            sourceID: "catokolas",
            enabled: true,
            installedRef: .gitCommit("abc"),
            config: ["middleClick": .object([
                "multiFinger": .object([
                    "fingerCount": .int(4),
                ]),
            ])])

        let snippet = SnippetGenerator.generate(
            state: state,
            catalog: ["MouseTrackpadTweaks": entry],
            repos: ["catokolas": catokolasRepo],
            timestamp: fixedDate)

        #expect(snippet.contains(
            "fn = function(s) s:configure({ " +
            "middleClick = { multiFinger = { fingerCount = 4 } } " +
            "}) end,"))
    }

